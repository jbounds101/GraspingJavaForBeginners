package lesson_02.solution;

public class SolutionVariables {
    public static void main(String[] args) {
        int myInt = 10;

        double myDouble = 1.322;
        char myChar = 'A';
        boolean myBoolean = false;

        System.out.println("Value of myInt: " + myInt);
        System.out.println("Value of myDouble: " + myDouble);
        System.out.println("Value of myChar: " + myChar);
        System.out.println("Value of myBool: " + myBoolean);
    }
}
