package lesson_01.tutorial;

public class TutorialHelloWorld {
    public static void main(String[] args) { /* <- This line is where code execution will begin, so we need to place
                                                all of our code inside this block (a block of code is a region
                                                confined between curly braces) */

        // Notice how code runs sequentially from the top-down
        System.out.println("I love programming!");
        System.out.println("Programming is so fun!");
        System.out.println("Sometimes it can get difficult!");
        System.out.println("But we never give up!");
    } // <- This is the end of the main() block
}

